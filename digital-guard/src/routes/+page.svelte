<h1>Wilkommen zu Digital Guard</h1>
<p>In einer Welt, in der unser digitales Leben immer mehr an Bedeutung gewinnt, stellen wir die Frage: 
    Was passiert mit Ihren Daten, Konten und digitalen Besitztümern, wenn Sie einmal nicht mehr da sind? 
    Mit Digital Guard bieten wir Ihnen eine sichere und einfache Lösung, um Ihren digitalen Nachlass zu organisieren und Ihre Wünsche für den Umgang damit festzulegen.

    Verwalten Sie all Ihre digitalen Konten an einem Ort.
    Hinterlassen Sie klare Anweisungen für den Todesfall.
    Bestimmen Sie vertrauenswürdige Personen, die Zugriff erhalten.
    Schützen Sie Ihre Daten mit höchsten Sicherheitsstandards.
    Übernehmen Sie die Kontrolle über Ihre digitale Zukunft - mit Digital Guard. Sicher. Einfach. Für Sie.</p>
